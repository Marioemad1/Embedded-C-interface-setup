//the libs we need
#define F_CPU 16000000UL
#include <util/delay.h>
#include"STO_TYPES.h"
#include"BIT_MATH.h"
#include"ATMEGA32_REG.h"
/* MCAL*/
#include"DIO_Interface.h"


int main(void)
{
	u8 switchstate=1;
	DIO_SetPinDirection(DIO_PORTA,DIO_PIN0,DIO_PIN_INPUT);
	DIO_SetPinDirection(DIO_PORTB,DIO_PIN0,DIO_PIN_OUTPUT);
    while (1)
    {
	    DIO_GetPin(DIO_PORTA,DIO_PIN0,&switchstate);
	    if(1==switchstate)
	    {
		    DIO_SetPin(DIO_PORTB,DIO_PIN0,DIO_PIN_HIGH);
		    _delay_ms(500);
	    }
	    else
	    {
		    DIO_SetPin(DIO_PORTB,DIO_PIN0,DIO_PIN_LOW);
		    _delay_ms(500);
	    }
		
    }
}

