#include "STO_TYPES.h"
#include "BIT_MATH.h"
#include "ATMEGA32_REG.h"
/* MCAL*/
#include "WDT_interface.h"

void WDT_start(void)
{
	//select the time of the WDT -->2.1ms
	SET_BIT(WDTCR,0);
	SET_BIT(WDTCR,1);
	SET_BIT(WDTCR,2);
	
	//enable the WDT timer
	SET_BIT(WDTCR,3);
}

void WDT_stop(void)
{
	 /* Write logical one to WDTOE and WDE */
	WDTCR |= (1<<3) | (1<<4);
	/* Turn off WDT */
	WDTCR = 0x00;
}