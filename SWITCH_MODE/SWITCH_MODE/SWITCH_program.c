/*UTILSLIB*/
#include "STO_TYPES.h"
#include "BIT_MATH.h"
#include "ATMEGA32_REG.h"
/* MCAL*/
#include "DIO_Interface.h"
/*HAL*/
#include "SWITCH_interface.h"

/*FUNCTIONS*/
void SWITCH_init(u8 PortId,u8 PinId)
{
	DIO_SetPinDirection(PortId,PinId,DIO_PIN_INPUT);
}
void SWITCH_state(u8 PortId,u8 PinId,u8* switch_state)
{
	u8 state;
	DIO_GetPin(PortId,PinId,&state);
	
	if (DIO_PIN_HIGH==state)
	{
		*switch_state = SWITCH_PRESSED;
	}
	else if (DIO_PIN_LOW==state)
	{
		*switch_state = SWITCH_NOT_PRESSED;
	}
}
